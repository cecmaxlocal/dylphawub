// Here is a simple example of a `main.cpp` file for a basic C++ program:

// ```cpp
#include <iostream>

using namespace std;

// Main function - entry point of the program
int main() {
    std::cout << "Hello, World!" << std::endl; // Print a message
    return 0; // Return success code
}
// ```

// Explanation:
// - `#include <iostream>` imports the input-output stream library.
// - `int main()` defines the main entry point function.
// - `std::cout` outputs text to the console.
// - `return 0;` indicates successful program termination.

